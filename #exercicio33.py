n1 = int(input('Digite um numero inteiro: '))
n2 = int(input('Digite outro numero inteiro: '))
n3 = int(input('Digite só mais um numero inteiro: '))
if n1 > n2 and n1 > n2:
    print (f'O numero {n1} é o maior deles')
elif n2 > n1 and n2 > n3:
    print (f'O numero {n2} é o maior deles')
else:
    print (f'O numero {n3} é o maior deles')

if n1 < n2 and n1 < n2:
    print (f'O numero {n1} é o menor deles')
elif n2 < n1 and n2 < n3:
    print (f'O numero {n2} é o menor deles')
else:
    print (f'O numero {n3} é o menor deles')