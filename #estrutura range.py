#estrutura range

#estrutura: 
#range(tamanho) ou range(inicio, fim) ou range(inicio, fim, passp)

#tamanho: 

for i in range(5):
    print(i)

#inicio e fim: 
print(range(1, 10))

for i in range(1, 10):
    print(i)

#range com passo: 
#esse conta de 0 a 100 pulando de 10 em 10
print(range(0, 10,2))

for i in range(0, 1000, 10):
    print(i)